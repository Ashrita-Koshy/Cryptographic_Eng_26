// round constants for key expansion
static const uint8_t rcon[7] = {
    0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40
};

// xtime in gf(2^8)
static uint8_t xtime(uint8_t a)
{
    return (uint8_t)((a << 1) ^ (((a >> 7) & 1) * 0x1b));
}

// expand 32-byte aes-256 key into round keys
static void key_expansion(const uint8_t key[32], uint8_t rk[240])
{
    int i;
    uint8_t temp[4];
    int rcon_idx = 0;

    memcpy(rk, key, 32);

    for (i = 32; i < 240; i += 4) {
        int word_idx = i / 4;

        temp[0] = rk[i - 4];
        temp[1] = rk[i - 3];
        temp[2] = rk[i - 2];
        temp[3] = rk[i - 1];

        // every 8th word gets rotword + subword + rcon
        if (word_idx % 8 == 0) {
            uint8_t t = temp[0];
            temp[0] = sbox[temp[1]] ^ rcon[rcon_idx++];
            temp[1] = sbox[temp[2]];
            temp[2] = sbox[temp[3]];
            temp[3] = sbox[t];
        }
        // middle extra subword step for aes-256
        else if (word_idx % 8 == 4) {
            temp[0] = sbox[temp[0]];
            temp[1] = sbox[temp[1]];
            temp[2] = sbox[temp[2]];
            temp[3] = sbox[temp[3]];
        }

        rk[i + 0] = rk[i - 32 + 0] ^ temp[0];
        rk[i + 1] = rk[i - 32 + 1] ^ temp[1];
        rk[i + 2] = rk[i - 32 + 2] ^ temp[2];
        rk[i + 3] = rk[i - 32 + 3] ^ temp[3];
    }
}

// substitute each byte using s-box
static void sub_bytes(uint8_t s[16])
{
    for (int i = 0; i < 16; i++)
        s[i] = sbox[s[i]];
}

// shift rows in aes state
static void shift_rows(uint8_t s[16])
{
    uint8_t tmp;

    // row 1 left shift by 1
    tmp  = s[1];
    s[1] = s[5]; s[5] = s[9]; s[9] = s[13]; s[13] = tmp;

    // row 2 left shift by 2
    tmp   = s[2];  s[2]  = s[10]; s[10] = tmp;
    tmp   = s[6];  s[6]  = s[14]; s[14] = tmp;

    // row 3 left shift by 3
    tmp   = s[3];
    s[3]  = s[15]; s[15] = s[11]; s[11] = s[7]; s[7] = tmp;
}

// mix columns in aes state
static void mix_columns(uint8_t s[16])
{
    for (int c = 0; c < 4; c++) {
        uint8_t *col = s + 4 * c;
        uint8_t a0 = col[0], a1 = col[1], a2 = col[2], a3 = col[3];

        col[0] = xtime(a0) ^ (xtime(a1) ^ a1) ^ a2 ^ a3;
        col[1] = a0 ^ xtime(a1) ^ (xtime(a2) ^ a2) ^ a3;
        col[2] = a0 ^ a1 ^ xtime(a2) ^ (xtime(a3) ^ a3);
        col[3] = (xtime(a0) ^ a0) ^ a1 ^ a2 ^ xtime(a3);
    }
}

// xor round key into state
static void add_round_key(uint8_t s[16], const uint8_t *rk)
{
    for (int i = 0; i < 16; i++)
        s[i] ^= rk[i];
}

// public aes-256 single-block encrypt function
void aes256_ecb_encrypt(const uint8_t key[32],
                        const uint8_t plaintext[16],
                        uint8_t       ciphertext[16])
{
    uint8_t rk[240];
    uint8_t state[16];

    // build round keys first
    key_expansion(key, rk);

    // start from plaintext
    memcpy(state, plaintext, 16);

    // initial addroundkey
    add_round_key(state, rk);

    // 13 full rounds
    for (int round = 1; round < 14; round++) {
        sub_bytes(state);
        shift_rows(state);
        mix_columns(state);
        add_round_key(state, rk + round * 16);
    }

    // final round has no mixcolumns
    sub_bytes(state);
    shift_rows(state);
    add_round_key(state, rk + 224);

    memcpy(ciphertext, state, 16);

    // clear expanded round keys from memory
    memset(rk, 0, sizeof(rk));
}